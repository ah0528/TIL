# 문자열의 구성 확인하기
# 1. isalpha() : 문자열에 숫자, 공백, 특수문자가 아닌 문자로 구성되어 있을 때만 True
# 2. isdigit() : 문자열이 모두 숫자로만 구성되어 있을 때만 True
# 3. isalnum() : 문자열이 공백, 특수문자가 아닌 문자와 숫자로만 구성되어 있을 때만 True
# 4. isspace() : 문자열이 모두 공백 문자로 구성되어 있을 때만 True
# 5. isupper() : 문자열이 모두 로마자 대문자로 구성되어 있을 때만 True
# 6. islower() : 문자열이 모두 로마자 소문자로 구성되어 있을 때만 True

print('41586'.isdigit())
print('41n586'.isdigit())